function initPlaceCards() {
    document.querySelectorAll('.place-card').forEach((card, i) => {
        card.addEventListener('click', () => openWorldModal(
            `${magicalPlaces[i].icon} ${magicalPlaces[i].name}`,
            magicalPlaces[i].details
        ));
    });
}

function initCreatureCards() {
    document.querySelectorAll('.creature-card').forEach((card, i) => {
        card.addEventListener('click', () => openWorldModal(
            `${thaiCreatures[i].emoji} ${thaiCreatures[i].name}`,
            thaiCreatures[i].details
        ));
    });
}

function initWorldModal() {
    const modal = document.getElementById('worldModal');
    const closeBtn = document.getElementById('worldModalClose');

    if (closeBtn) closeBtn.addEventListener('click', closeWorldModal);
    if (modal) modal.addEventListener('click', e => {
        if (e.target === modal) closeWorldModal();
    });
    document.addEventListener('keydown', e => {
        if (e.key === 'Escape' && modal && modal.classList.contains('active')) closeWorldModal();
    });
}

function openWorldModal(title, content) {
    const modal = document.getElementById('worldModal');
    document.getElementById('worldModalTitle').innerHTML = title;
    document.getElementById('worldModalBody').innerHTML = content;
    modal.classList.add('active');
    document.body.style.overflow = 'hidden';
}

function closeWorldModal() {
    document.getElementById('worldModal').classList.remove('active');
    document.body.style.overflow = '';
}
